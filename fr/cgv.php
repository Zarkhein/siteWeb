            <h4>Article 5 - OBLIGATIONS DU DONNEUR D'ORDRE</h4>
            <h5>5.1. - Emballage et étiquetage:</h5>
            <h6>5.1.1. - Emballage:</h6>
            <p>
              La marchandise doit être conditionnée, emballée, marquée ou
              contremarquée, de façon à supporter un transport et/ou une
              opération de stockage exécutés dans des conditions normales, ainsi
              que les manutentions successives qui interviennent nécessairement
              pendant le déroulement de ces opérations.
            </p>
            <p>
              Elle ne doit pas constituer une cause de danger pour les
              personnels de conduite ou de manutention, l’environnement, la
              sécurité des engins de transport, les autres marchandises
              transportées ou stockées, les véhicules ou les tiers.
            </p>
            <p>
              Le donneur d’ordre répond seul du choix du conditionnement et de
              son aptitude à supporter le transport et la manutention.
            </p>
            <h6>5.1.2 - Étiquetage:</h6>
            <p>
              Sur chaque colis, objet ou support de charge, un étiquetage clair
              doit être effectué pour permettre une identification immédiate et
              sans équivoque de l’expéditeur, du destinataire, du lieu de
              livraison et de la nature de la marchandise. Les mentions des
              étiquettes doivent correspondre à celles qui figurent sur le
              document de transport. L’étiquetage doit en outre satisfaire à
              toute réglementation applicable notamment celle relative aux
              produits dangereux.
            </p>
            <h6>5.1.3. - Responsabilité:</h6>
            <p>
              Le donneur d’ordre répond de toutes les conséquences d’une
              absence, d’une insuffisance ou d’une défectuosité du
              conditionnement, de l’emballage, du marquage ou de l’étiquetage.
            </p>
            <h5>5.2. - Plombage:</h5>
            <p>
              Les camions, les semi-remorques, les caisses mobiles, les
              conteneurs, complets une fois les opérations de chargement
              terminées, sont plombés par le chargeur lui-même ou par son
              représentant.
            </p>
            <h5>5.3. - Obligations déclaratives:</h5>
            <p>
              Le donneur d’ordre répond de toutes les conséquences d’un
              manquement à l’obligation d’information et de déclaration sur la
              nature très exacte et de la spécificité de la marchandise quand
              cette dernière requiert des dispositions particulières, eu égard
              notamment à sa valeur et/ou aux convoitises qu’elle est
              susceptible de susciter, de sa dangerosité ou de sa fragilité.
              Cette obligation d’information s’applique également à la
              déclaration de la masse brute vérifiée d’un conteneur conformément
              à la Convention SOLAS. Par ailleurs, le donneur d’ordre s’engage
              expressément à ne pas remettre à l’O.T.L. des marchandises
              illicites ou prohibées (par exemple des produits de contrefaçon,
              des stupéfiants, etc.).
            </p>
            <p>
              Le donneur d'ordre supporte seul, sans recours contre l’O.T.L.,
              les conséquences, quelles qu'elles soient, résultant de
              déclarations ou documents erronés, incomplets, inapplicables, ou
              fournis tardivement, en ce comprises les informations nécessaires
              à la transmission de toute déclaration exigée par la
              réglementation douanière, notamment pour les transports de
              marchandises en provenance de pays tiers.
            </p>
            <h5>5.4. - Réserves:</h5>
            <p>
              En cas de perte, d’avarie ou de tout autre dommage subi par la
              marchandise, ou en cas de retard, il appartient au destinataire ou
              au réceptionnaire de procéder aux constatations régulières et
              suffisantes, de prendre des réserves motivées et en général
              d'effectuer tous les actes utiles à la conservation des recours et
              à confirmer lesdites réserves dans les formes et les délais
              légaux, faute de quoi aucune action ne pourra être exercée contre
              l’O.T.L. ou ses substitués.
            </p>
            <h5>5.5 - Refus ou défaillance du destinataire:</h5>
            <p>
              En cas de refus des marchandises par le destinataire, comme en cas
              de défaillance de ce dernier pour quelque cause que ce soit, tous
              les frais initiaux et supplémentaires dus et engagés pour le
              compte de la marchandise resteront à la charge du donneur d'ordre.
            </p>
            <h5>5.6. - Formalités douanières:</h5>
            <p>
              Si des opérations douanières doivent être accomplies, le donneur
              d'ordre garantit le représentant en douane de toutes les
              conséquences financières découlant d'instructions erronées, de
              documents inapplicables, etc. entraînant d'une façon générale une
              liquidation de droits et/ou de taxes supplémentaires, un blocage
              ou saisie des marchandises, des amendes, etc. de l’administration
              concernée.
            </p>
            <p>
              En cas de dédouanement de marchandises au bénéfice d’un régime
              préférentiel conclu ou accordé par l’Union européenne, le donneur
              d’ordre garantit avoir fait toutes diligences au sens de la
              règlementation douanière visant à s’assurer que toutes les
              conditions pour le traitement du régime préférentiel ont été
              respectées.
            </p>
            <p>
              Le donneur d’ordre doit, sur demande de l’O.T.L., fournir à ce
              dernier, dans le délai requis, toute information qui lui sera
              réclamée au titre des exigences de la réglementation douanière. La
              non-fourniture de ces informations dans ce délai a pour effet de
              rendre responsable le donneur d’ordre de toutes les conséquences
              préjudiciables de ce manquement au titre de retards, surcoûts,
              avaries, etc.
            </p>
            <p>
              Toutefois, les règles de qualité et/ou de normalisation technique
              des marchandises relevant de la seule responsabilité du donneur
              d’ordre, il lui appartient de fournir à l’O.T.L. tous documents
              (tests, certificats, etc.) exigés par la réglementation pour leur
              circulation. L’O.T.L. n’encourt aucune responsabilité du fait de
              la non-conformité des marchandises auxdites règles de qualité ou
              de normalisation technique.
            </p>
            <p>
              Le représentant en douane dédouane sous le mode de la
              représentation directe, conformément à l’article 18 du Code des
              Douanes de l’Union.
            </p>
            <h5>5.7 - Livraison contre remboursement:</h5>
            <p>
              La stipulation d’une livraison contre remboursement ne vaut pas
              déclaration de valeur et ne modifie donc pas les règles
              d’indemnisation pour pertes et avaries telles que définies à
              l’article 6 ci-dessous.
            </p>
            <h5>5.8 - Attestation de vigilance:</h5>
            <p>
              Pour tout contrat d’un montant minimum de 5 000 € hors taxes , le
              donneur d’ordre doit obtenir de la part de son cocontractant
              l’attestation de vigilance certifiant qu’il est à jour de ses
              obligations sociales dès la conclusion du contrat et
              périodiquement tous les six mois jusqu’à la fin de l’exécution du
              contrat.
            </p>
            <p>
              Un numéro de sécurité est mentionné sur l’attestation. Afin de
              vérifier l’authenticité du document, le donneur d’ordre doit
              saisir le numéro de sécurité sur la page d’accueil du site
              www.urssaf.fr. En cas de non-respect par le donneur d’ordre de ses
              obligations de vigilance, des sanctions sont applicables.
            </p>
            <p>
              "Décret 2016-418 du 7 Avril - Les entreprises étrangères de
              transport seront tenues d'appliquer la réglementation sociale
              française aux salariés détachés dès lors qu'ils sont amenés à
              opérer sur le territoire français."
            </p>
