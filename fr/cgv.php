            <h4>Article 4 - EXECUTION DES PRESTATIONS</h4>
            <p>
              Les dates de départ et d'arrivée éventuellement communiquées par
              l’O.T.L. sont données à titre purement indicatif. Le donneur
              d’ordre est tenu de donner en temps utile les instructions
              nécessaires et précises à l’O.T.L. pour l’exécution des
              prestations de transport et des prestations accessoires et/ou des
              prestations logistiques.
            </p>
            <p>
              L’O.T.L. n'a pas à vérifier les documents (facture commerciale,
              note de colisage, etc.) fournis par le donneur d’ordre.
            </p>
            <p>
              Toutes instructions spécifiques à la livraison (contre
              remboursement, déclaration de valeur ou assurance, intérêt spécial
              à la livraison, etc.) doivent faire l’objet d'un ordre écrit et
              répété pour chaque envoi et de l’acceptation expresse de l’O.T.L.
            </p>
