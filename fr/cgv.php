            <h4>Article 2 - PRIX DES PRESTATIONS</h4>
            <p>
              <strong>2.1</strong> - Les prix sont calculés sur la base des
              informations fournies par le donneur d'ordre, en tenant compte
              notamment des prestations à effectuer, de la nature, du poids, et
              du volume de la marchandise à transporter et des itinéraires à
              emprunter. Les cotations sont établies en fonction du taux des
              devises au moment où lesdites cotations sont données. Elles sont
              également fonction des conditions et tarifs des substitués ainsi
              que des lois, règlements et conventions internationales en
              vigueur. Si un ou plusieurs de ces éléments de base se trouvaient
              modifiés après la remise de la cotation, y compris par les
              substitués de l’O.T.L., de façon opposable à ce dernier, et sur la
              preuve rapportée par celui-ci, les prix donnés primitivement
              seraient modifiés dans les mêmes conditions. Il en serait de même
              en cas d’événement imprévu, quel qu’il soit, entraînant notamment
              une modification de l’un des éléments de la prestation.
            </p>
            <p>
              <strong>2.2</strong> - Les prix ne comprennent pas les droits,
              taxes, redevances et impôts dus en application de toute
              réglementation notamment fiscale ou douanière.
            </p>
            <p>
              <strong>2.3</strong> - Les prix initialement convenus sont
              renégociés au moins une fois par an.
            </p>
