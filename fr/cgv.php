            <div class="article">
              <h4>Article 9 - PRESCRIPTION</h4>
              <p>
                Toutes les actions auxquelles le contrat conclu entre les
                parties peut donner lieu, que ce soit pour les prestations
                principales ou accessoires, sont prescrites dans le délai d’un
                an à compter de l’exécution de la prestation litigieuse dudit
                contrat et, en matière de droits et taxes recouvrés a
                posteriori, à compter de la notification du redressement.
              </p>
            </div>
            <div class="article">
              <h4>Article 10 – DUREE DU CONTRAT ET RESILIATION</h4>
              <p>
                <strong>10.1</strong> -En cas de relation commerciale établie,
                chaque partie peut y mettre fin à tout moment, par l’envoi d’une
                lettre recommandée avec avis de réception, sous réserve de
                respecter les délais de préavis suivants :
              </p>
              <ul>
                <li>
                  Un (1) mois quand la durée de la relation est inférieure ou
                  égale à six (6) mois ;
                </li>
