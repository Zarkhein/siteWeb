            <h4>Article 3 - ASSURANCE DES MARCHANDISES</h4>
            <p>
              Aucune assurance n'est souscrite par I’O.T.L. sans ordre écrit et
              répété du donneur d'ordre pour chaque expédition, précisant les
              risques à couvrir et les valeurs à garantir.
            </p>
            <p>
              Si un tel ordre est donné, l’O.T.L., agissant pour le compte du
              donneur d’ordre, contracte une assurance auprès d'une compagnie
              d'assurance notoirement solvable au moment de la couverture. A
              défaut de spécification précise, seuls les risques ordinaires
              (hors risques de guerre et de grève) seront assurés.
            </p>
            <p>
              Intervenant dans ce cas précis comme mandataire, l’O.T.L. ne peut
              être considéré en aucun cas comme assureur. Les conditions de la
              police d’assurance sont réputées connues et agréées par les
              expéditeurs et les destinataires qui en supportent le coût. Un
              certificat d'assurance sera émis, s’il est demandé.
            </p>
