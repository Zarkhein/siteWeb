            <h4>Article 1 - OBJET ET DOMAINE D’APPLICATION</h4>
            <p>
              Les présentes conditions générales ont pour objet de régir les
              relations contractuelles entre un donneur d’ordre et un
              ‘‘Opérateur de transport et/ou de logistique’’, ci-après dénommé
              l’O.T.L., au titre de tout engagement ou opération quelconque en
              lien avec le déplacement physique, par tout mode de transport,
              et/ou la gestion physique ou juridique de stocks et flux de toute
              marchandise, emballée ou non, de toute provenance et pour toute
              destination et/ou en lien avec la gestion de tout flux
              d’informations matérialisé ou dématérialisé.
            </p>
            <p>
              Les définitions des termes et notions utilisés dans les présentes
              conditions générales sont celles des contrats types en vigueur.
            </p>
            <p>
              Les présentes conditions générales prévalent sur toutes autres
              conditions générales ou particulières émanant du donneur d’ordre.
            </p>
            <p>
              En cas de conditions particulières convenues avec le donneur
              d’ordre et dans le silence de ces dernières, les conditions
              générales continuent à s’appliquer.
            </p>
