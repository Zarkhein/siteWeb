              <h4>Article 7 - CONDITIONS DE PAIEMENT</h4>
              <p>
                <strong>7.1</strong>- Les prestations de service sont payables
                comptant à réception de la facture, sans escompte, au lieu de
                l’émission de celle-ci, et en tout état de cause, dans un délai
                qui ne peut excéder 30 jours à compter de sa date d’émission. Le
                donneur d’ordre est toujours garant de leur acquittement.
                Conformément à l’article 1344 du Code civil, le débiteur est
                réputé avoir été mis en demeure de payer par la seule
                exigibilité de l’obligation.
              </p>
              <p>
                <strong>7.2</strong>- La compensation unilatérale du montant des
                dommages allégués sur le prix des prestations dues est
                interdite.
              </p>
              <p>
                <strong>7.3</strong>
                out retard dans le paiement entraîne de plein droit, le jour
                suivant la date de règlement figurant sur la facture,
                l’exigibilité d’intérêts de retard d’un montant équivalent au
                taux d’intérêt appliqué par la Banque Centrale Européenne (BCE)
                à son opération de refinancement la plus récente majoré de dix
                points de pourcentage et fixé selon les modalités définies à
                l’article L.441-6 alinéa 12 du Code de commerce, ainsi que d’une
                indemnité forfaitaire pour frais de recouvrement d’un montant de
                40 € suivant l’article D.441-5 du Code de commerce, et ce sans
                préjudice de la réparation éventuelle, dans les conditions du
                droit commun, de tout autre dommage résultant directement de ce
                retard. Tout retard de paiement emportera, sans formalités,
                déchéance du terme de toute autre créance détenue par l’O.T.L.
                qui devient immédiatement exigible même en cas d’acceptation
                d’effets.
              </p>
              <p>
                <strong>7.4</strong>- Tout paiement partiel sera imputé en
                premier lieu sur la partie non privilégiée de la créance.
              </p>
              <h4>
                Article 8 - DROIT DE RETENTION CONVENTIONNEL ET DROIT DE GAGE
                CONVENTIONNEL
              </h4>
              <p>
                Quelle que soit la qualité en laquelle l’O.T.L. intervient, le
                donneur d'ordre lui reconnaît expressément un droit de rétention
                conventionnel, opposable à tous, et un droit de gage
                conventionnel sur toutes les marchandises, valeurs et documents
                en possession de l’O.T.L., et ce en garantie de la totalité des
                créances (factures, intérêts, frais engagés, etc.) que l’O.T.L.
                détient contre lui, même antérieures ou étrangères aux
                opérations effectuées au regard des marchandises, valeurs et
                documents qui se trouvent effectivement entre ses mains.
              </p>
