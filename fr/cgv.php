            <h4>Article 6 – RESPONSABILITE</h4>
            <p>
              En cas de préjudice prouvé imputable à l’O.T.L., celui-ci n’est
              tenu que des dommages et intérêts qui pouvaient être prévus lors
              de la conclusion du contrat et qui ne comprennent que ce qui est
              une suite immédiate et directe de l’inexécution au sens des
              articles 1231-3 et 1231-4 du Code civil. Ces dommages et intérêts
              sont strictement limités conformément aux montants fixés
              ci-dessous. Ces limitations d’indemnités indiquées ci-dessous
              constituent la contrepartie de la responsabilité assumée par
              l’O.T.L.
            </p>
            <h5>6.1. - Responsabilité du fait des substitués:</h5>
            <p>
              La responsabilité de l’O.T.L. est limitée à celle encourue par les
              substitués dans le cadre de l’opération qui lui est confiée. Quand
              les limites d’indemnisation des substitués ne sont pas connues,
              sont inexistantes ou ne résultent pas de dispositions impératives,
              elles sont réputées identiques à celles fixées à l’article 6.2
              ci-après.
            </p>
            <h5>
              6.2. - Responsabilité personnelle de l’Opérateur de transport
              et/ou de logistique (O.T.L.):
            </h5>
            <h6>6.2.1. - Pertes et avaries:</h6>
            <p>
              Dans tous les cas où la responsabilité personnelle de l’O.T.L.
              serait engagée, pour quelque cause et à quelque titre que ce soit,
              elle est strictement limitée, pour tous les dommages à la
              marchandise imputables à toute opération par suite de pertes et
              avaries et pour toutes les conséquences pouvant en résulter, à 20
              € par kilogramme de poids brut de marchandises manquantes ou
              avariées sans pouvoir excéder, quels que soient le poids, le
              volume, les dimensions, la nature ou la valeur de la marchandise
              concernée, une somme supérieure au produit du poids brut de la
              marchandise exprimé en tonnes multiplié par 5.000 € avec un
              maximum de 60.000 € par événement.
            </p>
            <h6>6.2.2. - Autres dommages:</h6>
            <p>
              Pour tous les autres dommages, y compris en cas de retard de
              livraison dûment constaté, au cas où sa responsabilité personnelle
              serait engagée, la réparation due par l’O.T.L. est strictement
              limitée au prix du transport de la marchandise (droits, taxes et
              frais divers exclus) ou à celui de la prestation à l’origine du
              dommage, objet du contrat. Cette indemnité ne pourra excéder celle
              qui est due en cas de perte ou d’avarie de la marchandise.
            </p>
            <h6>6.2.3. - Responsabilité en matière douanière:</h6>
            <p>
              La responsabilité de l’O.T.L. pour toute opération en matière de
              douane ou de contribution indirecte qu’elle soit réalisée par ses
              soins ou par ceux de ses sous-traitants ne pourra excéder la somme
              de 5.000 € par déclaration en douane, sans pouvoir excéder 50.000
              € par année de redressement et, en toute hypothèse, 100.000 € par
              notification de redressement.
            </p>
            <h6>6.3. - Cotations:</h6>
            <p>
              Toutes les cotations données, toutes les offres de prix
              ponctuelles fournies, ainsi que les tarifs généraux sont établis
              et/ou publiés en tenant compte des limitations de responsabilité
              ci-dessus énoncées (6.1 et 6.2)
            </p>
            <h6>6.4. - Déclaration de valeur ou assurance:</h6>
            <p>
              Le donneur d’ordre a toujours la faculté de souscrire une
              déclaration de valeur qui, fixée par lui et acceptée par l’O.T.L.,
              a pour effet de substituer le montant de cette déclaration aux
              plafonds d’indemnité indiqués ci-dessus (Articles 6.1 et 6.2.1).
              Cette déclaration de valeur entraînera un supplément de prix.
            </p>
            <p>
              Le donneur d’ordre peut également donner instructions à l’O.T.L.,
              conformément à l’Article 3 (Assurance des marchandises), de
              souscrire pour son compte une assurance, moyennant le paiement de
              la prime correspondante, en lui précisant les risques à couvrir et
              les valeurs à garantir.
            </p>
            <p>
              Les instructions (déclaration de valeur ou assurance) doivent être
              renouvelées pour chaque opération.
            </p>
            <h6>6.5. - Intérêt spécial à la livraison:</h6>
            <p>
              Le donneur d’ordre a toujours la faculté de faire une déclaration
              d’intérêt spécial à la livraison qui, fixée par lui et acceptée
              par l’O.T.L., a pour effet, en cas de retard, de substituer le
              montant de cette déclaration aux plafonds d’indemnité indiqués
              ci-dessus (Articles 6.1 et 6.2.2). Cette déclaration entraînera un
              supplément de prix. Les instructions doivent être renouvelées pour
              chaque opération.
            </p>
