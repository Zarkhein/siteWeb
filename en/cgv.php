            <div class="article">
              <h4>Article 10 – CONTRACTUAL POSSESSORY LIEN</h4>
              <p>
                Whatever the capacity in which the .T.L.O acts the instructing
                party formally acknowledges its contractual surety right
                including a possessory lien and general preferential right on
                all the goods, valuables and documents held by the carriage
                operator, to guarantee all claims debts (invoice, interests,
                incurred costs, etc.) that the T.L.O has against it, even those
                previous or foreign to the transactions completed with the
                goods, valuables and documents effective held by it.
              </p>
            </div>
            <div class="article">
              <h4>Article 11 – PRESCRIPTION PERIOD</h4>
              <p>
                All claims arising from the contract entered into by the parties
                shall be time barred within one year after the performance of
                the disputed service of the said contract and in the area of
                duties and taxes collected afterwards, as from the adjustment
                notification.
              </p>
            </div>
            <div class="article">
              <h4>Article 12 – CONTRACT TERM AND TERMINATION</h4>
              <p>
                <strong>12.1</strong> - In the event the instructing party and
                the T.L.O enter into an open-ended contract materialising
                lasting relations that the parties wish to establish, the
                contract may be terminated at any time by either party by
                registered mail with receipt confirmation subject to one month
                notice when the time elapsed since the commencement of the
                performance of the contract does not exceed six months. Such
                notice is increased to two months when such time exceeds six
                months and is under one year. When the relationship has lasted
                for more than one year, the notice time is increased to three
                months, plus one month per year of continuous relations after
                the two year period, not exceeding a period of six months.
              </p>
              <p>
                <strong>12.2</strong> - During the notice time the parties agree
                to maintain the balance of the contract.
              </p>
              <p>
                <strong>12.3</strong>In the event of serious or repeated proven
                breaches by any of the parties in the performance of its
                obligations the other party shall send it a motivated injunction
                by registered mail with receipt confirmation. In the event that
                injunction is unsuccessful for one month, during which period
                the parties may attempt to negotiate, the contract shall be
                terminated without notice or compensation by registered mail
                with receipt confirmation acknowledging the failure of the
                negotiation attempt.
              </p>
              <p>
                <strong>12.4</strong> All claims pertaining to the above
                provisions shall be time barred within one year in accordance
                with the provisions of above-mentioned article 11 (PRESCRIPTION
                PERIOD).
              </p>
            </div>
            <div class="article">
              <h4>Article 13 – CANCELLATION - INVALIDITY</h4>
              <p>
                Should any of the provisions of these General Terms of sale be
                found invalid or cancelled, all other provisions shall remain in
                full force and effect.
              </p>
            </div>
            <div class="article">
              <h4>Article 14 – JURISDICTIONAL CLAUSE</h4>
              <p>
                In the event of any dispute or complaint, the venue shall be the
