            <h4>Article 1 – PURPOSE AND SCOPE</h4>
            <p>
              The purpose of these terms is to set forth the terms of
              performance by a “Transport and / or Logistics Operator”,
              hereinafter referred to as the TLO in any capacity (airfreight
              forwarder, shipping agent, freight forwarder “commissionnaire de
              transport”, freight broker, warehouse keeper, agent, handler,
              accredited or non customs broker “commissionnaire en Douane agréé
              ou non”, forwarding agent, carrier, etc.), of services and
              activities pertaining to the physical movement of shipments and/or
              to the management of flows of goods, whether packed or not, of all
              kinds, of all origins, to all destinations, in exchange for a
              freely agreed price ensuring fair compensation for the services
              performed, both domestically and internationally.
            </p>
            <p>
              Any undertaking or transaction with “the Transport and / or
              Logistics Operator” constitutes unreserved acceptance by the
              instructing party of the terms set forth below.
            </p>
            <p>
              Whatever the carriage method used, these terms shall govern the
              relationship between the instructing party and “the Transport and
              / or Logistics Operator”.
            </p>
            <p>
              “The Transport and / or Logistics Operator” performs the requested
              services according to the terms specified, inter alia, in article
              7 below.
            </p>
            <p>
              No specific term or other general term of the instructing party
              shall prevail over these terms, unless as otherwise formally
              accepted by the “Transport and / or Logistics Operator”.
            </p>
          </div>
          <div class="article">
            <h4>Article 2 - DEFINITIONS</h4>
            <p>
              In the meaning of these General Terms, the following terms are
              defined as follows:
            </p>
            <p><strong>2.1. INSTRUCTING PARTY:</strong></p>
            <p>
              Instructing party shall mean the party ordering the service from
              the Transport and / or Logistics Operator, or even the Customs
              broker.
            </p>
            <p><strong>2.2. PARCEL:</strong></p>
            <p>
              Parcel shall mean an item or material set made up of several
              items, whatever the weight, size and volume thereof, making up a
              unit load upon delivery for transport (bin, cage, crate, coffer
              carton container, envelope, burden, drum, paquet, circled or
              film-wrapped pallet, roll, bag, suitcase, etc.), packed by the
              sender before pick-up even if the contents are detailed in the
              shipping document.
            </p>
            <p><strong>2.3. SHIPMENT: </strong></p>
            <p>
              Shipment shall mean the quantity of goods, packages and load
              support effectively provided, at the same time, to the Transport
              and / or Logistics Operator and whose displacement is requested by
              one single instructing party towards one single consignee from a
              single place of loading to a single place of unloading and
              reproduced in one single document.
            </p>
          </div>
