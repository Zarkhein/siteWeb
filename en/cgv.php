          <div class="article">
            <h4>Article 4 – GOODS INSURANCE</h4>
            <p>
              No insurance is taken out by the. T.L.O without a written and
              repeated order by the instructing party for each shipment,
              indicating risks to be covered and values to be insured.
            </p>
            <p>
              If such an order is given, the T.L.O, acting on behalf of the
              instructing party, shall take out insurance with an insurance
              company that is creditworthy at the time of coverage. Failing
              specific indicators, only ordinary risks (apart from war and
              strike risks) shall be covered.
            </p>
            <p>
              As it acts, in this specific case, as an agent, the T.L.O shall
              not be considered as an insurer.
            </p>
            <p>
              The terms of the policy are deemed known and approved by the
              senders and the consignees, who shall bear the cost thereof. An
              insurance certificate shall be issued, on request.
            </p>
          </div>
          <div class="article">
            <h4>Article 5 – PERFORMANCE OF THE SERVICES</h4>
            <p>
              Departure and arrival dates possibly provided by the T.L.O are
              given for informational purposes only.
            </p>
            <p>
              The instructing party shall give, in due time, all necessary and
              accurate instructions to the T.L.O for the performance of the
              transport services and of related services and/or logistics
              services. It is not within the T.L.O’s duty to check the documents
              (commercial invoice, packing note, etc.) provided by the
              instructing party. All delivery-specific instructions (cash on
              delivery, etc.) shall be written in a written and repeated order
              for each shipment and formally accepted by the T.L.O At any rate,
              such an agency constitutes an accessory of the main carriage
              service and / or of the logistics service.
            </p>
          </div>
          <div class="article">
            <h4>Article 6 - INSTRUCTING PARTY’S OBLIGATIONS</h4>
            <h5>6.1. Packing and labelling:</h5>
            <h6>6.1.1. Packing:</h6>
            <p>
              The goods should be packed, packaged, marked or countermarked so
              as to withstand transport and / or storage performed in normal
              conditions, as well as successive handling, which necessarily
              occur during the sequence of such operations.
            </p>
            <p>
              They should not constitute a hazard to driving or handling
              personnel, the environment, transport vehicle safety, other
              carried or stored goods, vehicles or third parties.
            </p>
            <p>
              The instructing party shall be solely responsible for packaging
              and for its fitness to bear the transport and handling.
            </p>
            <p>
              Should the instructing party entrust the T.L.O with goods in
              breach of the above provisions, it shall be solely responsible,
              with no claim against the T.L.O for any damage they may cause.
            </p>
            <h6>6.1.2. Labelling:</h6>
            <p>
              On each parcel, item or load support, clear labelling should be
              provided to allow immediate and clear identification of the
              sender, of the consignee, of the place of delivery and of the
              nature of the goods.
            </p>
            <p>
              The statements on the labels should match those shown on the
              shipping document.
            </p>
            <h6>6.1.3. Liability:</h6>
            <p>
              The instructing party shall be responsible for all consequences of
              any lack of, or inadequacy or defect in, packing, packaging,
              marking or labelling.
            </p>
            <h5>6.2. Sealing:</h5>
            <p>
              Full trucks, semi-trailers, mobiles crates, containers, after the
              loading operations are completed, should be sealed by the loader
              himself or it representative.
            </p>
            <h5>6.3. Declaratory obligations:</h5>
            <p>
              The instructing party shall be responsible for all consequences of
              any failure to perform the duty of information and declaration
              regarding the specific nature and the specificity of the goods,
              when the latter requires specific provisions, including the value
              thereof and / or any covetousness that it may cause, or its
              dangerousness or fragility. In addition, the instructing party
              formally agrees not to provide the T.L.O with illegal or
              prohibited goods (for instance, infringing products, narcotics,
              etc.).
            </p>
            <p>
              The instructing party shall bear alone, with no claim against the
              T.L.O, any consequences of erroneous, incomplete, unenforceable or
              belated declarations or documents including but not limited to
              information needed for the provision of any brief declaration
              required by customs regulations, including for goods transports
              from third countries.
            </p>
            <h5>6.4. Reserves:</h5>
            <p>
              In the event of any loss or damage sustained by the goods, or of
              any delay, the consignee or receiver is responsible for making
              regular and adequate inspections, expressing motivated reserves
              and generally carrying out any action useful for the protection of
              claims and for confirming said reserves in legal forms and
              timeframes, failing which no claim may be exercised against the
              T.L.O or its representatives.
            </p>
            <h5>6.5. Refusal or failure by the consignee:</h5>
            <p>
              In the event the goods are refused by the consignee, and in the
              event of the latter’s failure for any reason, all initial and
              additional costs owed and incurred in connection with the goods
              shall be borne by the instructing party.
            </p>
            <h5>6.6. Customs formalities:</h5>
            <p>
              If customs transactions need to be completed, the instructing
              party shall hold the customs agent harmless against any financial
              consequences arising from erroneous instructions, unusable
              documents, etc. generally leading to the payment of additional
              duties and / or taxes, fines, etc. to the respective public
              authorities.
            </p>
            <p>
              In the event the goods are customs cleared under a preferential
              status
            </p>
            <p>
              entered into or granted by the European Union, the instructing
              party guarantees that it has taken all steps in the meaning of the
              Community Customs Code, to ensure that all conditions for the
              preferential status process have been fulfilled.
            </p>
            <p>
              The instructing party shall on the T.L.O’s request, provide the
              latter, within the requested time, with any information requested
              from it in connection with customs regulations requirements.
              Failure to provide such information within such time shall cause
              the instructing party to be responsible for any harmful
              consequences of such failure in connection with delays,
              over-charges, damages, etc.
            </p>
            <p>
              However, as goods quality and / or technical standardization rules
              are the responsibility of the instructing party, it shall be
              responsible for providing the T.L.O with all documents (tests,
              certificates, etc.) required by regulations for the circulation of
              the goods. The T.L.O shall not be responsible for any failure by
              the goods to comply with the said quality or technical
              standardization rules.
            </p>
            <p>
              The accredited customs broker “commissionnaire en Douane agréé”
              clears in direct representation mode, in accordance with article 5
              of the Community Customs Code.
            </p>
