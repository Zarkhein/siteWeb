                of the services is allowed.
              </p>
              <p>
                If payment terms are granted, they shall not exceed thirty days
                as from the invoice date, for all services performed by freight
                forwarder “commissionnaire de transport” and road carriers, as
                well as those performed by shipping agent / or airfreight
                forwarder, by customs brokers “commissionnaire en Douane”,
                freight brokers and forwarding agent in accordance with the
                provisions of article L.441-6 of the Business Code.
              </p>
              <p>
                Any partial payment on the agreed term shall be applied firstly
                to the unsecured fraction of the claim. Failure to pay any
                single term shall result, with no formality whatsoever, in an
                event of default, and the balance shall become immediately
                payable even in the event of commercial paper acceptance.
                Penalties shall be automatically applied should outstanding
                amounts be paid after the agreed payment date. Such penalties
                result from the absolute provisions of article L.441-6 of the
                Business Code and shall be applied in full. The payability date
                and late payment penalty interest rate are shown in the invoice.
              </p>
