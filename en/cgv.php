          <div class="article">
            <h4>Article 7 - LIABILITY</h4>
            <h5>7.1. Liability for substituted parties’ actions:</h5>
            <p>
              The liability of the T.L.O is limited to the liability incurred by
              the substituted parties in the framework of the transaction
              entrusted to it. When the compensation thresholds of
              intermediaries or substituted parties are unknown or do not result
              from absolute or legal provisions, they shall be deemed equal to
              those set in article 7.2. below.
            </p>
            <h5>
              7.2. Personal liability of the Transport and / or Logistics
              Operator (T.L.O):
            </h5>
            <p>
              The compensation limitations shown below are consideration for the
              liability borne by the T.L.O
            </p>
            <h6>7.2.1. Losses and damages:</h6>
            <p>
              In all cases where the. T.L.O’s personal liability is incurred,
              for any reason and in any capacity, it shall be strictly limited
              to damages to the goods involved in the transport as a result of
              losses and damages and for any consequences resulting therefrom,
              to EUR 17.25 per kilogram of gross weight of missing or damaged
              goods, without exceeding, whatever the weight, volume, sizes,
              nature or value of the respective goods, an amount exceeding the
              product of the gross weight of the goods expressed in tons
              multiplied by EUR 2 850 with a maximum amount of EUR 60 000 per
              event.
            </p>
            <h6>7.2.2. Other damages:</h6>
            <p>
              For all other damages, including duly acknowledged delivery
              delays, should its personal liability be incurred, the
              compensation owed by the Transport and / or Logistics Operator
              shall be strictly limited to the price of the goods transport
              (exclusive of duties, taxes and miscellaneous costs) or to that of
              the service leading to the damage, under the contract. Such
              compensation shall not exceed the compensation that is owed in the
              event of goods damage or loss.
            </p>
            <p>
              For any damage resulting from a failure in the performance of the
              logistics service, under the contract, the compensation owed by
              the Logistics Operator, in the event its personal liability is
              incurred, shall be strictly limited to the amount of the service
              leading to the damage and shall not exceed a maximum amount of EUR
              60 000 per event.
            </p>
            <p>
              In no event shall the. T.L.O’s liability exceeds the
              above-mentioned amounts.
            </p>
            <h5>7.3. Quotations</h5>
            <p>
              All quotations given, all occasional price offers provided, as
              well as general price lists, are established and / or published
              based on the above-mentioned liability limitations (7.1. and
              7.2.).
            </p>
            <h6>7.4. Value or insurance declaration:</h6>
            <p>
              The instructing party may also instruct the .T.L.O, in accordance
              with Article 4 (Goods insurance), to take out insurance on its
              behalf, in exchange for the settlement of the respective premium,
              and by indicating risks to be covered and values to be insured.
            </p>
            <p>
              The instructions (declaration of value or assurance) should be
              renewed for each transaction.
            </p>
            <h6>7.5. Special interest in delivery:</h6>
            <p>
              The instructing party may always make a statement of special
              interest in delivery which, set by it and accepted by the T.L.O,
              results in substituting the amount of that declaration to the
              compensation limits specified above (Articles 7.1 and 7.2.2.).
              Such a declaration shall result in an extra price. Such
              instructions should be renewed for each transaction.
            </p>
