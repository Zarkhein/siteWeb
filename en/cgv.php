            <h4>Article 3 – PRICE OF THE SERVICES</h4>
            <p>
              <strong>3.1</strong>– Prices are calculated on the basis of the
              information provided by the instructing party, considering, inter
              alia, the services to be provided, the nature, the weight, and the
              volume of the goods to be carried and routes to be used.
              Quotations are prepared based on currency rates at the time when
              the said quotations are given. They are also based on the terms
              and prices of substituted parties and on applicable laws,
              regulations and international conventions in effect. Should one or
              more of the above base items be modified after the quotation is
              provided, including by the substituted parties of the T.L.O, in a
              manner binding upon the latter, and upon the evidence furnished by
              it, prices given initially shall be changed on the same terms. The
              same shall apply in the event of an unforeseen event, whatever it
              may be, leading to a change in any part of the service. This
              applies, inter alia to fuel prices, whose variation should be
              taken into account, in accordance with the provisions of articles
              L. 3222-1 and L. 3222-2 of the Transport Code.
            </p>
            <p>
              <strong>3.2</strong>– Prices do not include duties, taxes, fees
              and taxation owed in pursuance of any tax or customs or other
              regulations (such as excise, entry duties, etc.).
            </p>
            <p>
              Intervenant dans ce cas précis comme mandataire, l’O.T.L. ne peut
              être considéré en aucun cas comme assureur. Les conditions de la
              police d’assurance sont réputées connues et agréées par les
              expéditeurs et les destinataires qui en supportent le coût. Un
              certificat d'assurance sera émis, s’il est demandé.
            </p>
            <p>
              <strong>3.3</strong>– Prices initially agreed shall be
              renegotiated at least once a year on the anniversary date of the
              contract. They shall also be revised in the event of significant
              changes in the T.L.O’s expenses, which expenses are most often due
              to conditions beyond the T.L.O’s control, such as fuel prices, as
              specified in the above paragraph (3.1.). If the parties fail to
              reach an agreement on new price terms, each party may terminate
              the contract according to the terms set in article 12 below.
            </p>
